from django.contrib.auth import get_user_model
from django.test import TestCase

from ..models import Group, Post

User = get_user_model()


class PostModelTest(TestCase):
    @classmethod
    def setUpClass(cls):
        super().setUpClass()
        cls.user = User.objects.create_user(username='auth')
        cls.group = Group.objects.create(
            title='Тестовая группа',
            slug='Тестовый слаг',
            description='Тестовое описание',
        )
        cls.post = Post.objects.create(
            author=cls.user,
            # в комментарии написано "подлиннее",
            # я посчитал что это опечатка и определил как "по длиннее".
            # Правильно?))
            text='Тестовый пост для проверки обрезки после 15 символов',
        )

    def test_verbose_name(self):
        """verbose_name видна на страницах с пользовательскими формами."""
        post = PostModelTest.post
        field_verboses = {
            'text': 'Текст поста',
            'group': 'Группа'
        }
        for value, expected in field_verboses.items():
            with self.subTest(value=value):
                self.assertEqual(
                    post._meta.get_field(value).verbose_name, expected)

    def test_help_text(self):
        """help_text видна на страницах с пользовательскими формами."""
        post = PostModelTest.post
        field_help_texts = {
            'text': 'Текстовое поле',
            'group': 'Группа, к которой будет относиться пост',
        }
        for value, expected in field_help_texts.items():
            with self.subTest(value=value):
                self.assertEqual(
                    post._meta.get_field(value).help_text, expected)

    def test_models_have_correct_object_names(self):
        """Проверяем, что у моделей корректно работает __str__."""
        group = PostModelTest.group
        post = PostModelTest.post
        expected_object_post = post.text[:15]
        expected_object_group = group.title
        self.assertEqual(expected_object_post, str(post))
        self.assertEqual(expected_object_group, str(group))
